package math;

public class Maths {
	public static void main(String[] args) {

		int a = 9;
		int b = 4;
		int c;

		c = a + b;
		System.out.printf("a+b = %d \n", c);

		c = a - b;
		System.out.printf("a-b = %d \n", c);

		c = a * b;
		System.out.printf("a*b = %d \n", c);

		c = a / b;
		System.out.printf("a/b = %d \n", c);

		c = a % b;
		System.out.printf("Remainder when a divided by b = %d \n", c);

	}
}
